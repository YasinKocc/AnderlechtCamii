// LocalStorage'da dil tercihini saklayın ve sayfayı güncelleyin
function changeLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    updatePageLanguage(lang);
}

// Sayfa içeriğini seçilen dile göre güncelleyin
function updatePageLanguage(lang) {
    const selectedLanguage = languages[lang];

    // Header güncelleme
    document.querySelector('.header-right a[href="index2.html"]').textContent = selectedLanguage.header.home;
    document.querySelector('.header-right a[href="Admins.html"]').textContent = selectedLanguage.header.board;
    document.querySelector('.header-right a[href="AGK.html"]').textContent = selectedLanguage.header.agk;
    document.querySelector('.header-right a[href="contact.html"]').textContent = selectedLanguage.header.contact;
    document.querySelector('.header-right a[href="Membership.html"]').textContent = selectedLanguage.header.membership;

    // Banner güncelleme
    document.querySelector('.slide-content h1').textContent = selectedLanguage.banner.title;
    document.querySelector('#banner-button').textContent = selectedLanguage.banner.button;

    // Main Box'ları güncelleme
    document.querySelector('.main-box1 h2').textContent = selectedLanguage.main.box1.title;
    document.querySelector('.main-box1 p').textContent = selectedLanguage.main.box1.description;
    document.querySelector('#yazilma-formu').textContent = selectedLanguage.main.box1.button;

    document.querySelector('.main-box2 h2').textContent = selectedLanguage.main.box2.title;
    document.querySelector('.main-box2 p').textContent = selectedLanguage.main.box2.description;
    document.querySelector('#genclik-button').textContent = selectedLanguage.main.box2.button;

    document.querySelector('.main-box3 h2').textContent = selectedLanguage.main.box3.title;
    document.querySelector('.main-box3 p').textContent = selectedLanguage.main.box3.description;
    document.querySelector('#hakkinda-button').textContent = selectedLanguage.main.box3.button;

    // Yönetim Kurulu başlığı güncelleme
    document.querySelector('.admin-container h1').textContent = selectedLanguage.adminsPage.title;

    // Yönetim Kurulu üyeleri güncelleme
    const persons = document.querySelectorAll('.person');
    selectedLanguage.adminsPage.persons.forEach((person, index) => {
        if (persons[index]) {
            persons[index].querySelector('h3').textContent = person.name;
            persons[index].querySelector('p').textContent = person.job;
            persons[index].setAttribute('onclick', `openModal('${person.name}', '${person.job}', '${person.description}', 'images/user.png')`);
        }
    });

    // Dropdown menü güncelleme
    document.querySelector('#dropdown-imams').textContent = selectedLanguage.adminsPage.dropdowns.imams;
    document.querySelector('#dropdown-past-presidents').textContent = selectedLanguage.adminsPage.dropdowns.pastPresidents;

    // Hikayemiz bölümü güncelleme
    document.querySelector('.hakkinda-part2 h2').textContent = selectedLanguage.hikayemiz.title;
    document.querySelector('.hakkinda-part2 p').textContent = selectedLanguage.hikayemiz.description;

    // Adres bölümü güncelleme
    document.querySelector('.camii-map1 h2').textContent = selectedLanguage.adres.title;
    document.querySelector('.camii-map1 p').textContent = selectedLanguage.adres.address;
    document.querySelector('#map-button').textContent = selectedLanguage.adres.button;

    // Footer güncelleme
    document.querySelector('.footer-help a[href="Membership.html"]').textContent = selectedLanguage.footer.form;
    document.querySelector('.footer-help a[href="Admins.html"]').textContent = selectedLanguage.footer.board;

    // Dil seçimi butonu güncelleme
    document.querySelector('.dropdown-btn').textContent = selectedLanguage.languageButton;
}

// Sayfa yüklendiğinde dil tercihini kontrol edin ve uygun dilde güncelleyin
window.onload = function() {
    const savedLanguage = localStorage.getItem('selectedLanguage');
    if (savedLanguage) {
        updatePageLanguage(savedLanguage);
    } else {
        updatePageLanguage('tr'); // Varsayılan dil Türkçe
    }
};
